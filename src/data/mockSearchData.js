// Function to search through mock documents
export const searchMockDocuments = (query, filters = {}) => {
  if (!query || query.trim() === '') {
    return [];
  }

  const searchTerm = query.toLowerCase().trim();
  let results = mockDocuments.filter(doc => {
    // Search in title, content, and keywords
    const titleMatch = doc.title.toLowerCase().includes(searchTerm);
    const contentMatch = doc.content.toLowerCase().includes(searchTerm);
    const keywordMatch = doc.keywords.some(keyword => 
      keyword.toLowerCase().includes(searchTerm) || 
      searchTerm.includes(keyword.toLowerCase())
    );
    
    return titleMatch || contentMatch || keywordMatch;
  });

  // Apply filters
  if (filters.category && filters.category !== 'all') {
    results = results.filter(doc => doc.category === filters.category);
  }

  if (filters.fileType && filters.fileType !== 'all') {
    results = results.filter(doc => doc.fileType === filters.fileType);
  }

  // Sort by relevance score (higher first)
  results.sort((a, b) => b.relevanceScore - a.relevanceScore);

  // Format results for display
  return results.map(doc => ({
    id: doc.id,
    title: doc.title,
    content: doc.content,
    category: doc.category,
    fileType: doc.fileType,
    lastUpdated: doc.lastUpdated,
    relevanceScore: doc.relevanceScore,
    searchType: doc.searchType,
    highlights: doc.highlights,
    timestamp: new Date().toISOString()
  }));
};

